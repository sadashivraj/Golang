package main

import "fmt"

func main(){

	fmt.Printf("%d \n", 100) //Format printing decimals
	fmt.Printf("%b \n", 100) //Binary
	fmt.Printf("%x \n", 100) //Hex
	fmt.Printf("%o \n", 100) //Octal
	fmt.Printf("%#x \n", 100) //Leading 0 for Hex
	fmt.Printf("%q \n", 65) //UTF-8


}

